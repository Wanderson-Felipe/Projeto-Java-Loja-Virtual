    private void btnexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnexcluirActionPerformed

        int id;
        ProdutoDAO dao = new ProdutoDAO();
        boolean status = dao.conectar();
        if (status == false) {
            JOptionPane.showMessageDialog(null, "erro de conexão");
        } else {
            int selectedRow = tblproduto.getSelectedRow();
            if (selectedRow != -1) {

                id = Integer.parseInt(tblproduto.getValueAt(selectedRow, 0).toString());
                status = dao.excluir(id);
            }

            if (status == true) {
                JOptionPane.showMessageDialog(null, "Produto Excluido com Sucesso!");
                preencherTabela("");
                limparTela();

                btnexcluir.setEnabled(false);
                btnalterar.setEnabled(false);
                btnsalvar.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "Erro na exclusão do produto!");

            }
            dao.desconectar();

        }


    }//GEN-LAST:event_btnexcluirActionPerformed

    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
        Produto produto = new Produto();
        ProdutoDAO dao = new ProdutoDAO();
        boolean status;
        int resposta;

        try {

            String nome = txtnome.getText();
            String tamanho = txttamanho.getText();
            String descricao = txtdescricao.getText();
            String quantidade = txtquantidade.getText();
            String valor = txtvalor.getText();

            status = dao.conectar();

            if (!status) {
                JOptionPane.showMessageDialog(null, "Erro de conexão");
                return;
            }

            produto.setNome(nome);
            produto.setTamanho(tamanho);
            produto.setDescricao(descricao);
            produto.setQuantidade(quantidade);
            produto.setValor(valor);

            resposta = dao.salvar(produto);

            if (resposta == 1) {
                JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso");
                preencherTabela("");
                limparTela();

            } else {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar o Produto");
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {

            dao.desconectar();

        }
    }//GEN-LAST:event_btnsalvarActionPerformed

    private void tblprodutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblprodutoMouseClicked
        int linhaSelecionada = tblproduto.getSelectedRow();
        if (linhaSelecionada != -1) {

            String id = tblproduto.getValueAt(linhaSelecionada, 0).toString();
            String nome = tblproduto.getValueAt(linhaSelecionada, 1).toString();
            String tamanho = tblproduto.getValueAt(linhaSelecionada, 2).toString();
            String descricao = tblproduto.getValueAt(linhaSelecionada, 3).toString();
            String quantidade = tblproduto.getValueAt(linhaSelecionada, 4).toString();
            String valor = tblproduto.getValueAt(linhaSelecionada, 5).toString();

            txtnome.setText(nome);
            txttamanho.setText(tamanho);
            txtdescricao.setText(descricao);
            txtquantidade.setText(quantidade);
            txtvalor.setText(valor);

            btnexcluir.setEnabled(true);
            btnalterar.setEnabled(true);
            btnsalvar.setEnabled(false);
    }//GEN-LAST:event_tblprodutoMouseClicked
    }
    private void btnalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnalterarActionPerformed

        ProdutoDAO dao = new ProdutoDAO();
        boolean status;
        int resposta;

        int selectedRow = tblproduto.getSelectedRow();
        if (selectedRow != -1) {
            int id = Integer.parseInt(tblproduto.getValueAt(selectedRow, 0).toString());

            Produto produto = new Produto();
            produto.setId(id);
            produto.setNome(txtnome.getText());
            produto.setTamanho(txttamanho.getText());
            produto.setDescricao(txtdescricao.getText());
            produto.setQuantidade(txtquantidade.getText());
            produto.setValor(txtvalor.getText());

            status = dao.conectar();

            status = dao.conectar();

            if (!status) {
                JOptionPane.showMessageDialog(null, "Erro de conexão");
                return;
            }

            resposta = dao.alterar(produto);

            if (resposta == 1) {
                JOptionPane.showMessageDialog(null, "Produto alterado com sucesso");
                preencherTabela("");
                limparTela();

            } else {
                JOptionPane.showMessageDialog(null, "Erro ao alterar o produto");

            }

            dao.desconectar();

        }

    }//GEN-LAST:event_btnalterarActionPerformed

