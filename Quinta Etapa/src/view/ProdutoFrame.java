    private void preencherTabela(String filtro) {

        ProdutoDAO dao = new ProdutoDAO();
        boolean status = dao.conectar();

        if (status == false) {
            JOptionPane.showMessageDialog(null, "erro de conexão");
        } else {
            List<Produto> listaProduto = dao.listagem(filtro);
            DefaultTableModel tabelaProduto = (DefaultTableModel) tblproduto.getModel();
            tblproduto.setRowSorter(new TableRowSorter(tabelaProduto));
            tabelaProduto.setNumRows(0);

            for (Produto p : listaProduto) {
                Object[] obj = new Object[]{
                    p.getId(),
                    p.getNome(),
                    p.getTamanho(),
                    p.getDescricao(),
                    p.getQuantidade(),
                    p.getValor()};
                tabelaProduto.addRow(obj);
            }
        }
    }

